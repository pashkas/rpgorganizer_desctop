    /// </summary>
    public class minMaxChaConverter : IValueConverter
    {
        #region Public Methods and Operators

        /// <summary>
        /// The convert.
        /// </summary>
        /// <param name="value">
        /// The value.
        /// </param>
        /// <param name="targetType">
        /// The target type.
        /// </param>
        /// <param name="parameter">
        /// The parameter.
        /// </param>
        /// <param name="culture">
        /// The culture.
        /// </param>
        /// <returns>
        /// The <see cref="object"/>.
        /// </returns>
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            Characteristic cha = value as Characteristic;

            if (parameter != null && parameter.ToString() == "минимум")
            {
                return 0;
            }

            if (parameter != null && parameter.ToString() == "максимум")
            {
                return 10;
            }

            return 0;
        }

